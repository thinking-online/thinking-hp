def build():
    css = C.build_css(RUNNING) + R.READING_EXTRA_CSS
    body = []
    body.append(C.cover("第 3 部 - Ⅱ / 大問Ⅱ", "総合長文読解 攻略ドリル", RUNNING, badge="総合長文"))
    body.append(FRONT)
